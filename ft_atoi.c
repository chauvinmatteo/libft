#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int signe;
	int res;

	i = 0;
	res = 0;
	signe = 1;

	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (signe * res);
}